#include <iostream>
using namespace std;
int main() {
  
  char ch = 'A';
  int s, j, k, m;
  for (s = 1; i <= 5; s++) {
    
    for (j = 5; j >= s; j--) cout << " ";
    for (k = 1; k <= i; k++) cout << ch++;
    ch--;
    for (m = 1; m < s; m++) cout << --ch;
    cout << "\n";
    ch = 'A';
  }
  
  return 0;
  
}
